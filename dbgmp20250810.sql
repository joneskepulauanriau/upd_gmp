DROP PROCEDURE IF EXISTS `peringkatPemain`$$
CREATE DEFINER=`root`@`localhost` PROCEDURE `peringkatPemain` (IN `id_turnamen_max` INT)  BEGIN
    -- Hapus data lama sebelum menyimpan data baru
    DELETE FROM peringkat WHERE id_turnamen = id_turnamen_max;

    -- Masukkan data peringkat dengan total_poin dan jumlah_turnamen yang benar
    INSERT INTO peringkat (id_turnamen, id_pemain, total_poin, jumlah_turnamen)
    SELECT 
        id_turnamen_max,  -- Gunakan ID turnamen terbaru
        p.id_pemain,
        SUM(p.poin) AS total_poin,
        COUNT(DISTINCT p.id_turnamen) AS jumlah_turnamen  -- Hitung jumlah turnamen yang diikuti
    FROM pertandingan p
    WHERE p.id_turnamen = id_turnamen_max
    GROUP BY p.id_pemain;

END$$

